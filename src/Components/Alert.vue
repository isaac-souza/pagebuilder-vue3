<script>
    import { defineComponent, computed, watch } from 'vue'
    import { useStore } from 'vuex'

    import { ACTION_DISMISS_ALERT } from '../Utils/action-types'

    export default defineComponent({
        name: 'Alert',
        setup() {
            const store = useStore()

            const show = computed(() => {
                return store.state.alert.show
            })

            const type = computed(() => {
                return store.state.alert.type
            })

            const message = computed(() => {
                return store.state.alert.message
            })

            watch(
                () => store.state.alert.show,
                (show) => {
                    if(show) {
                        setTimeout(() => {
                            store.dispatch(ACTION_DISMISS_ALERT)
                        }, 4000)
                    }
                }
            )

            const dismiss = () => {
                store.dispatch(ACTION_DISMISS_ALERT)
            }

            return { store, dismiss, show, type, message, }
        },
    })
</script>

